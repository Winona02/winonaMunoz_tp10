let num1 = 40;
let num2 = 35;

//a. SUMA
let suma = num1 + num2;
console.log("El resultado de " + num1 + " + " + num2 + " es: " + suma);

//b. RESTA
let resta = num1 - num2;
console.log("El resultado de " + num1 + " - " + num2 + " es: " + resta);

//c. MULTIPLICACIÓN
let multiplicacion = num1 * num2;
console.log("El resultado de " + num1 + " x " + num2 + " es: " + multiplicacion);

//d. DIVISIÓN
let division = num1 / num2;
console.log("El resultado de " + num1 + " / " + num2 + " es: " + division);

//e.MÓDULO
let modulo = num1 % num2;
console.log("El resultado de " + num1 + " % " + num2 + " es: " + modulo);

//f. ¿Cuál de los valores es mayor?
let esMayor = num1>num2
console.log("¿Es verdad que " + num1 + " es mayor que " + num2 + " ? " + esMayor);

//g. ¿Cuál de los valores es menor?
let esMenor = num1<num2
console.log("¿Es verdad que " + num1 + " es menor que " + num2 + " ? " + esMenor);

